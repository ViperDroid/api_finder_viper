def fetch_js_content(js_url):
    """
    Fetch the content of a JavaScript file.
    """
    try:
        response = requests.get(js_url, headers=HEADERS, timeout=5)
        response.raise_for_status()
        return response.text
    except requests.RequestException as e:
        print(f"{Fore.YELLOW}[!] Error fetching {js_url}: {e}{Style.RESET_ALL}")
        return None

def find_apis_in_website(url):
    """
    Find APIs on a website by analyzing its HTML and JavaScript files.
    """
    try:
        print(f"{Fore.CYAN}[*] Scanning {url} for APIs...{Style.RESET_ALL}")
        response = requests.get(url, headers=HEADERS, timeout=5)
        response.raise_for_status()

        # Find APIs in HTML
        html = response.text
        apis = find_apis_in_text(html)

        # Find APIs in JavaScript files
        soup = BeautifulSoup(html, 'html.parser')
        script_tags = soup.find_all('script', src=True)
        js_urls = [urljoin(url, script['src']) for script in script_tags]

        # Fetch JavaScript files concurrently
        with ThreadPoolExecutor(max_workers=10) as executor:
            futures = {executor.submit(fetch_js_content, js_url): js_url for js_url in js_urls}
            for future in as_completed(futures):
                js_content = future.result()
                if js_content:
                    apis.update(find_apis_in_text(js_content))

        # Print results
        if apis:
            print(f"{Fore.GREEN}[+] Found APIs on {url}:{Style.RESET_ALL}")
            for api in sorted(apis):
                print(f"  - {api}")
        else:
            print(f"{Fore.RED}[-] No APIs found on {url}{Style.RESET_ALL}")

    except requests.RequestException as e:
        print(f"{Fore.YELLOW}[!] Error accessing {url}: {e}{Style.RESET_ALL}")

